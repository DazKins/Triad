package com.dazkins.triad.gfx;

public class WindowInfo {
	private int w, h;
	
	public WindowInfo(int w, int h) {
		this.w = w;
		this.h = h;
	}

	public int getW() {
		return w;
	}

	public int getH() {
		return h;
	}

	public void setW(int w) {
		this.w = w;
	}

	public void setH(int h) {
		this.h = h;
	}
}